# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitos:
# str, int, float, bool

print(int('1'), type(int('1')))

print(int('1'), + 1)

print(float('1.5') + 1) # quando inteiro + float o valor final será float

print(bool('')) # boleano vazio = false

print(str('11' + 'b'))