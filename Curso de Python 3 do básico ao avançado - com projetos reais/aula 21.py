"""
Operadores de comparação (relacionais)
OP         Significado         Exemplo (true)
>          maior               2 > 1
>=         maior ou igual      2 >= 2
<          menor               1 < 2
<=         menor ou igual      2 <= 2
==         igual               'a' == 'a'
!=         diferente           'a' != 'b'
"""

maior = 2 > 1
print(maior)

maior_igual = 2 >= 2
print(maior_igual)

menor = 1 < 2
print(menor)

menor_igual = 2 <= 2
print(menor_igual)

igual = 'a'== 'a'
print(igual)

diferente = 'a' != 'b'
print(diferente)